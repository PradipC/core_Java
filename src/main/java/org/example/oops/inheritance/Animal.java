package org.example.oops.inheritance;

public class Animal {

    public static void name(){
        System.out.println("My name is Animal");
    }


    public void sleep(){
        System.out.println("Animal can sleep");
    }


    public void walk(){
        System.out.println("Animal can walk");
    }

    public void eat(){
        System.out.println("Animal can eat");
    }






}
